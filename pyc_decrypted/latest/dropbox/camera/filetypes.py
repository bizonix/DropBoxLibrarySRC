def image_or_video(name):
    _, ext = os.path.splitext(name)
    ext = ext.lower()
    return ext in IMAGE_EXTENSIONS or ext in PHOTO_EXTENSIONS or ext in VIDEO_EXTENSIONS


def nonphoto_image(name):
    _, ext = os.path.splitext(name)
    if ext.lower() in IMAGE_EXT_BLACKLIST:
        return True
    return False
